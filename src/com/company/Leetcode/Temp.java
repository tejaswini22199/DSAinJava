package com.company.Leetcode;

public class Temp {
    private int rowId;
    private int count;

    public Temp(int rowId, int count) {
        this.rowId = rowId;
        this.count = count;
    }

    public int getRowId() {
        return rowId;
    }

    public int getCount() {
        return count;
    }
}
